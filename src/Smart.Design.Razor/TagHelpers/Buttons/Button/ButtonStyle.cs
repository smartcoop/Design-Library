namespace Smart.Design.Razor.TagHelpers.Buttons.Button;

/// <summary>
/// Valid button style of a Smart design button.
/// </summary>
public enum ButtonStyle
{
    /// <summary>
    /// Primary style of Smart Design button.
    /// </summary>
    Primary,
    /// <summary>
    /// Secondary style of Smart Design button.
    /// </summary>
    Secondary,
    /// <summary>
    /// Danger style of Smart Design Button.
    /// </summary>
    Danger,
    /// <summary>
    /// Secondary danger style to indicate a danger.
    /// </summary>
    DangerSecondary,
    /// <summary>
    /// Makes the button to be borderless.
    /// </summary>
    Borderless,
    /// <summary>
    /// Warning style of Smart Design Button.
    /// </summary>
    Warning
}
